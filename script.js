function showResultBoxAl() {
    quizBoxAl.classList.remove('active');
    resultBoxAl.classList.add('active');

    const scoreTextAl = document.querySelector('.score-textstal');
    scoreTextAl.textContent = `Your Score ${userScore} out of ${preintergh.length}`;

    const circularProgressal = document.querySelector('.circular-progressal');
    const progressValueal = document.querySelector('.progress-valueal');
    let progressStartValue = -1;
    let progressEndValue = (userScore / preintergh.length) * 100;
    progressEndValue = Math.round(progressEndValue);

    let speed = 20;

    let progress = setInterval(() => {
        progressStartValue++;

        progressValueal.textContent = `${progressStartValue}%`;
        circularProgressal.style.background = `conic-gradient(blue ${progressStartValue * 3.6}deg, rgba(255, 255, 255, .1) 0deg)`;

        if (progressStartValue == progressEndValue) {
            clearInterval(progress);
        }
    }, speed);
}


























